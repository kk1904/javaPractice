package controlstructure;

public class IfElseIfExample {
    public static void main(String[] args){
        int no=-5;
        if(no>0){
            System.out.println("no is positive");
        }
        else if(no<0){
            System.out.println("no is negative");
        }
        else{
            System.out.println("no is zero");
        }
    }
}
