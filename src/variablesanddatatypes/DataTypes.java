package variablesanddatatypes;

public class DataTypes {
    public static void main(String[] args)
    {

        boolean myBool=true;
        char myChar='k';
        byte myByteValue=32;
        short myShortValue=1904;
        int myIntegerNum=7;
        float myFloatNum=79.99f;
        long myLongNumber=190419912023l;
        double myDoubleValue=42.4344454647484950;

        //Non-Primitive Data-Type
        String myString="Krishna";

        System.out.println("Primitive DataType");
        System.out.println("Boolean:"+myBool);
        System.out.println("Char:"+myChar);
        System.out.println("Byte:"+myByteValue);
        System.out.println("Short:"+myShortValue);
        System.out.println("Integer:"+myIntegerNum);
        System.out.println("Float:"+myFloatNum);
        System.out.println("Long:"+myLongNumber);
        System.out.println("Double:"+myDoubleValue);

        System.out.println();
        System.out.println("Non-Primitive DataType");
        System.out.println("String:"+myString);
    }
}
